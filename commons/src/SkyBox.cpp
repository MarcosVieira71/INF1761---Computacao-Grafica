void SkyBox::Draw(StatePtr st)
{
    auto camera = st->GetCamera();

    glm::vec4 origin(0, 0, 0, 1);
    glm::vec3 eye = glm::vec3(glm::inverse(camera->GetViewMatrix()) * origin);

    glm::mat4 M = glm::translate(glm::mat4(1.0f), eye);

    st->PushMatrix();
    st->LoadMatrix(M);
    st->LoadMatrices();

    glDepthMask(GL_FALSE);

    glBindVertexArray(m_vao);
    glDrawArrays(GL_TRIANGLES, 0, 36);

    glDepthMask(GL_TRUE);

    st->PopMatrix();
}
